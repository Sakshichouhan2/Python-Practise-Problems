#Bitwise operators
Number1 = int(input("Enter Any number "))
Number2 = int(input("Enter Any number "))

AND = Number1 & Number2
OR = Number1 | Number2
NOT =(~ Number1)
XOR = Number1 ^ Number2
Right_shift = Number1 >> Number2
Left_shift = Number1 << Number2



print(AND)
print(OR)
print(NOT)
print(XOR)
print(Right_shift)
print(Left_shift)

